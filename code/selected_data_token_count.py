import json

with open(file_directory) as f:
    data_list = json.load(f)

#extract all input and output tokens
input_tokens = [item["input_tokens"] for item in data_list]
output_tokens = [item["output_tokens"] for item in data_list]

#Function to calculate median
def calculate_median(numbers):
  sorted_numbers = sorted(numbers)
  n = len(sorted_numbers)
  mid = n//2

  if n % 2 == 0:
    median = (sorted_numbers[mid-1] + sorted_numbers[mid])/2
  else:
    median = sorted_numbers[mid]

  return median

#Calculate medians
median_input = calculate_median(input_tokens)
median_output = calculate_median(output_tokens)

print(f"Median input tokens: {median_input}")
print(f"Median output tokens: {median_output}")